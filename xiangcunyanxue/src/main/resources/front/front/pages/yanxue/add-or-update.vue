            // },
			yanxuePhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.yanxuePhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			yanxueTimeConfirm(val) {
                this.ruleForm.yanxueTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.yanxueName)) {
                    this.$utils.msg(`研学标题不能为空`);
                    return
                }
				if ((!this.ruleForm.yanxueUuidNumber)) {
                    this.$utils.msg(`研学编号不能为空`);
                    return
                }
				if ((!this.ruleForm.yanxuePhoto)) {
                    this.$utils.msg(`研学照片不能为空`);
                    return
                }
				if ((!this.ruleForm.yanxueFuzeren)) {
                    this.$utils.msg(`负责人不能为空`);
                    return
                }
				if ((!this.ruleForm.yanxuePhone) && (!this.$validate.isMobile(this.ruleForm.yanxuePhone))) {
                    this.$utils.msg(`联系方式不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.yanxueAddress)) {
                    this.$utils.msg(`研学路线不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.yanxueKucunNumber) && (!this.$validate.isIntNumer(this.ruleForm.yanxueKucunNumber)) && this.ruleForm.yanxueKucunNumber >0) {
                    this.$utils.msg(`剩余可参与人数不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.yanxueTime)) {
                    this.$utils.msg(`出发时间不能为空`);
                    return
                }
				if ((!this.ruleForm.yanxueClicknum) && (!this.$validate.isIntNumer(this.ruleForm.yanxueClicknum)) && this.ruleForm.yanxueClicknum >0) {
                    this.$utils.msg(`研学热度不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`yanxue`, this.ruleForm);
                } else {
                    await this.$api.save(`yanxue`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
