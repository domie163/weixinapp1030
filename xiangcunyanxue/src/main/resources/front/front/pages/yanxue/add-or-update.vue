					yanxueTime: '',
					yanxueClicknum: '',
					yanxueContent: '',
					shangxiaTypes: '',//数字
					shangxiaValue: '',//数字对应的值
					yanxueDelete: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            yanxueTypesOptions: [],
            yanxueTypesIndex : 0,
            shangxiaTypesOptions: [],
            shangxiaTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let yanxueTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'yanxue_types',
            }
			let yanxueTypes = await this.$api.page(`dictionary`, yanxueTypesParams);
			this.yanxueTypesOptions = yanxueTypes.data.list
		/*下拉框*/
			let shangxiaTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shangxia_types',
            }
			let shangxiaTypes = await this.$api.page(`dictionary`, shangxiaTypesParams);
			this.shangxiaTypesOptions = shangxiaTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`yanxue`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.yanxueId){
                this.ruleForm.yanxueId = options.yanxueId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            yanxueTypesChange(e) {
                this.yanxueTypesIndex = e.target.value
                this.ruleForm.yanxueValue = this.yanxueTypesOptions[this.yanxueTypesIndex].indexName
                this.ruleForm.yanxueTypes = this.yanxueTypesOptions[this.yanxueTypesIndex].codeIndex
            },
            shangxiaTypesChange(e) {
                this.shangxiaTypesIndex = e.target.value
                this.ruleForm.shangxiaValue = this.shangxiaTypesOptions[this.shangxiaTypesIndex].indexName
                this.ruleForm.shangxiaTypes = this.shangxiaTypesOptions[this.shangxiaTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
